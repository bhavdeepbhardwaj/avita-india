    {
		  //
        $this->validate($request,[
            'email' => 'required'
        ]);
        $notify = new Notify([
                'email' => $request->get('email')
        ]);
        $notify->save();
        return redirect()->back()->with('message', 'Thank you for your submission. You shall receive a confirmation mail shortly!');

